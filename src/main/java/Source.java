public class Source {
    int brickAmount;
    int woodAmount;
    int wheatAmount;
    int sheepAmount;
    int oreAmount;

    public int getBrickAmount() {
        return brickAmount;
    }

    public void setBrickAmount(int brickAmount) {
        this.brickAmount = brickAmount;
    }

    public int getWoodAmount() {
        return woodAmount;
    }

    public void setWoodAmount(int woodAmount) {
        this.woodAmount = woodAmount;
    }

    public int getWheatAmount() {
        return wheatAmount;
    }

    public void setWheatAmount(int wheatAmount) {
        this.wheatAmount = wheatAmount;
    }

    public int getSheepAmount() {
        return sheepAmount;
    }

    public void setSheepAmount(int sheepAmount) {
        this.sheepAmount = sheepAmount;
    }

    public int getOreAmount() {
        return oreAmount;
    }

    public void setOreAmount(int oreAmount) {
        this.oreAmount = oreAmount;
    }
}
